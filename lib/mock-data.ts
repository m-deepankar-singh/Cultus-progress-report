// Helper function to calculate correct average stars
function calculateAverageStars(starDistribution: Array<{stars: number, count: number}>) {
  const totalRatings = starDistribution.reduce((sum, item) => sum + item.count, 0);
  if (totalRatings === 0) return 0;
  
  const starSum = starDistribution.reduce((sum, item) => sum + (item.stars * item.count), 0);
  return Number((starSum / totalRatings).toFixed(1));
}

